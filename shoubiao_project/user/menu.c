/**
  * @brief 初始化菜单
	* @param const char *Fname   菜单的名字
  * @param  const char * img   图片
  * @retval 无
  */
Function* Function_init(const char *Fname,const char * img ) //菜单初始化函数 
{
	Function *fc=malloc(sizeof(Function));  //使用malloc函数给新菜单分配空间，注意需要导入string.h 
	fc->Fname=Fname;  //初始化菜单名 
  fc->down=NULL;   //下级菜单的结构体
	fc->up=NULL;  //上级菜单的结构体
	fc->img=img;  //菜单的图片
	fc->Next=NULL;  //下一个菜单
	fc->Last=NULL;  //上一个菜单
	fc->Final=NULL; //最后一个菜单
	fc->task_handler=NULL;   //二级菜单可以绑定任务
	return fc;
}
/**
	* @brief 初始化主菜单和副菜单
	* @param Function *up_l   主菜单 这个是一对多的关系
  * @param Function *low_l  主菜单绑定的附属菜单
  * @retval 无
  */
void Funtction_up_list(Function *up_l,Function *low_l)
{
	if(up_l->down==NULL)  //如果当前菜单的下级菜单为空，就绑定，只需要绑定一个就够了
	{
		up_l->down=low_l;
	}
	low_l->up=up_l;
	
}
/**
	* @brief 初始化循环列表 
	* @param Function *Head   头节点 说是头节点，其实是第一个节点，用来确定当前链表的最后一个元素
  * @param Function *FinalList  要插入的节点
  * @retval 无
  */
void Function_List(Function *Head,Function *FinalList) //需传入头节点，和需要尾插的节点
{
	if(Head->Next==NULL) //头节点的下一个无任何节点 
	{
		Head->Next=FinalList;   //头节点的下一个
		Head->Last=FinalList;  //头文件的上一个是最后一个 这也就是循环的一部分
		Head->Final=FinalList;  //当前链表的最后一个元素
		FinalList->Last=Head; //直接将传入的尾节点插入尾部 
		FinalList->Final=FinalList;  //其实这里可以不用

	}
	else //有则使用双向链表尾插法 
	{
		FinalList->Last=Head->Final; //插入元素的上一个元素 是最后一个元素
		Head->Final->Next=FinalList; //最后一个元素的下一个元素是上一个元素
		FinalList->Next=Head;    //插入元素的下一个元素是头元素 这就形成了循环
		Head->Last=FinalList;   //头元素的上一个元素说是插入元素
		Head->Final=FinalList;  //更新当前链表的最后一个元素
		 

	}
}